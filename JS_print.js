//There are three different ways to print a statement in JS

//First method
// This method is used to write and display in browser console, majorly for debugging purpose.
console.log("Hello World");

//Second method
// This method is used to display the message in alert box, can also be written as window.alert()
alert("Hello, World!");

//Third method
// Used majorly for testing purpose, and should write after an HTML doc is loaded else it deletes all existing content
document.write("Hello, World!");
